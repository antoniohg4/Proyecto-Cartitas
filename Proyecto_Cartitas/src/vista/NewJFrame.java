    public void crearEspacioDeCartas(){
        panelCartas.setLayout(new java.awt.GridLayout(4,5));
        for (int i = 0; i < BTN_LONG; i++) {

                cartasItems.add(i, new javax.swing.JButton()); //inicio boton
                panelCartas.add(cartasItems.get(i)); //a√±ado boton al panel para que se vea
                ImageIcon fotoCarta = new ImageIcon(getClass().getResource("/resources/img/Incognita_card.png"));
                Image scaledImage = fotoCarta.getImage();
                scaledImage = scaledImage.getScaledInstance(fotoCarta.getIconWidth()/3, fotoCarta.getIconHeight()/3, java.awt.Image.SCALE_SMOOTH);
                fotoCarta = new ImageIcon(scaledImage);
                cartasItems.get(i).setIcon(fotoCarta);
                cartasItems.get(i).setBackground(new Color(245,245,245)); //color del fonto del boton
                cartasItems.get(i).setFocusable(false); //esto hace que no se pueda enfocar el boton y no salga un recuadro raro feo
                cartasItems.get(i).setFont(new Font("Arial",Font.BOLD,120)); //la font de las letras dentro del boton
        }

        panelCartas.setVisible(true);
    }

