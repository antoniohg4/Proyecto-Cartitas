    private void btnComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComprarActionPerformed
        if (Integer.parseInt(this.labelNumeroMonedas.getText()) <= 0) {
            this.setFocusable(false);
            this.dialogMercaderes.setVisible(true);
        }else{
            boolean comprado = gestion_BD.comprar(nombreJugador); 
            if (comprado) { //Si el update se ha hecho correctamente
                ArrayList<Carta> listaCartasSobre = Juego.abrirSobre(); //lista que se va a mostrar en el dialog
                this.labelNumeroMonedas.setText(String.valueOf(Integer.parseInt(this.labelNumeroMonedas.getText()) - 100)); //Actualiza el label de las monedas
                gestion_BD.anadirCartasAColeccion(listaCartasSobre, nombreJugador);
                mostarCartasSobre(listaCartasSobre);//Crea un JDialog para mostar las cartas que salieron en el sobre. 
                cargarFotosCartas();//se vuelve a cargar las cartas que est√°n en la base de datos.
            }
        }
    }//GEN-LAST:event_btnComprarActionPerformed

    private void mostarCartasSobre(ArrayList<Carta> listaCartasSobre){

        JDialog sobreAbiertoDialog= new javax.swing.JDialog();
        sobreAbiertoDialog.setLayout(new java.awt.GridLayout(1,3));
        for (Carta carta : listaCartasSobre) {
            System.out.println("idCarta: "+carta.getId());
            System.out.println("CartaURL: "+carta.getUrl());
            JLabel labelimagen = new JLabel();
            ImageIcon fotoCarta = new ImageIcon(getClass().getResource(carta.getUrl()));
            Image scaledImage = fotoCarta.getImage();
            scaledImage = scaledImage.getScaledInstance(fotoCarta.getIconWidth(), fotoCarta.getIconHeight(), java.awt.Image.SCALE_SMOOTH);
            fotoCarta = new ImageIcon(scaledImage);
            labelimagen.setIcon(fotoCarta);
            labelimagen.setSize(fotoCarta.getIconWidth(), fotoCarta.getIconHeight());
            sobreAbiertoDialog.add(labelimagen);
            sobreAbiertoDialog.setSize(fotoCarta.getIconWidth(), fotoCarta.getIconHeight());
            sobreAbiertoDialog.setLocationRelativeTo(null);
        }
        sobreAbiertoDialog.pack();
        sobreAbiertoDialog.setVisible(true);
        sobreAbiertoDialog.setDefaultCloseOperation(DISPOSE_ON_CLOSE);
    }

